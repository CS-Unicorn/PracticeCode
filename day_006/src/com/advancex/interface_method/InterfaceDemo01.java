package com.advancex.interface_method;

public class InterfaceDemo01 {
    private String name;
    private int age;

    public void setName(String nam) {
        name = nam;
    }

    public String getName() {
        return name;
    }

    public void setAge(int ag) {
        age = ag;
    }

    public int getAge() {
        return age;
    }
}
