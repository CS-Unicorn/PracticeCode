public class demo02StringCatenate{
	/**
		String Catenate
	*/
	public static void main(String[] args){
		char c1 = '╬м';
		System.out.println("c1 = " + c1);
		char c2 = 'ол';
		System.out.println("c1 = " + c2);
		System.out.println("c1+c2 = " + (c1+c2) + "\t" + "\"" + (char)c1 +(char)c2 + "\"");
	}
}