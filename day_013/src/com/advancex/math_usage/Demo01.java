package com.advancex.math_usage;

public class Demo01 {
    public static void main(String[] args) {
        System.out.println("Math.abs(-1) = " + Math.abs(-1));
        System.out.println("Math.sqrt(4) = " + Math.sqrt(4));
        System.out.println("Math.ceil(3.9) = " + Math.ceil(3.9));
        System.out.println("Math.ceil(3.2) = " + Math.ceil(3.2));
        System.out.println("Math.max(1,2) = " + Math.max(1, 2));
        System.out.println("Math.min(1,2) = " + Math.min(1, 2));
        System.out.println("Math.round(3.4) = " + Math.round(3.4));
        System.out.println("Math.round(-3.2) = " + Math.round(-3.2));
    }
}